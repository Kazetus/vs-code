"use strict";
import Request from "./module/Request.js";
/*************************************************************************************************/
/* ****************************************** DONNEES ****************************************** */
/*************************************************************************************************/
let request = new Request();
let input1;
let input2;
/*************************************************************************************************/
/* ***************************************** FONCTIONS ***************************************** */
/*************************************************************************************************/
function playModule(){
    request.getInput(input1.value, input2.value); 
}
/*************************************************************************************************/
/* ************************************** CODE PRINCIPAL *************************************** */
/*************************************************************************************************/
document.addEventListener('DOMContentLoaded', function(){
    input1 = document.getElementById("lastname");
    input2 = document.getElementById("firstname");
    $('#button').on("click", playModule);
});